    private boolean noiguales(String nombre, String telefono) {
        
        DefaultTableModel model = principal.getTablaContenidoModel();

  
            for (int i = 0; i < model.getRowCount(); i++) {

                Object nombreExistente = model.getValueAt(i, 0); 
                Object telefonoExistente = model.getValueAt(i, 3); 


                if ((nombreExistente != null && nombreExistente.toString().equals(nombre)) || 
                    (telefonoExistente != null && telefonoExistente.toString().equals(telefono))) {
                    return true; 
                    }
                }
            return false; 
    }
    
    
    
    
    
    
    
    private boolean validarCorreo(String correo) {
        return correo.contains("@") && correo.endsWith(".com");
        }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
   
