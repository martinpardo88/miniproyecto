    private void guardarbotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarbotonActionPerformed
    
        
        String nombre = camponombre.getText();
        String apellido = campoapellido.getText();
        String correo = campocorreo.getText();
        String telefono = campotelefono.getText();
        String direccion = campodireccion.getText();
        String estadoCivil = "";
            if (jRadioButton1.isSelected()) {
                estadoCivil = "Soltero";
            } else if (jRadioButton2.isSelected()) {
                estadoCivil = "Casado";
            } else if (jRadioButton3.isSelected()) {
                estadoCivil = "Divorciado";
            } else if (jRadioButton4.isSelected()) {
                estadoCivil = "Union Libre";
            }
    
        
        
        
        
        
        
        
        
            if (telefono.isEmpty()) {
                JOptionPane.showMessageDialog(this, "el número de teléfono no puede estar vacio.");
                return; 
            }




            
            if (!telefono.matches("\\d+")) {
                JOptionPane.showMessageDialog(this, "el numero de telefono debe contener solo numeros.");
                return;
            }

            
            
            
            
   
            if (!validarCorreo(correo)) {
                JOptionPane.showMessageDialog(this, "el correo electronico no es valido este debe contener '@' y terminar con '.com'.");
                return; 
            }

    
            
            
            
            if (noiguales(nombre, telefono)) {
                JOptionPane.showMessageDialog(this, "ya existe un contacto con ese nombre o número.");
                return;
            }

            
            
            
            
  
        DefaultTableModel model = principal.getTablaContenidoModel();
        model.addRow(new Object[]{nombre, apellido, correo, telefono, direccion, estadoCivil});

   
        
        
        this.dispose();
    
   
        
    }//GEN-LAST:event_guardarbotonActionPerformed

    
    
